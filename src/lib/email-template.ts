export function emailInfoCard(rows: Array<{ icon: IconName; label: string; value: string }>): string {
  const rowsHtml = rows
    .map(
      (r, i) => `
  <tr><td style="padding:11px 16px;${i < rows.length - 1 ? `border-bottom:1px solid ${BRAND.rowBorder};` : ""}">
    <table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0"><tr>
      <td valign="middle">
        <table role="presentation" cellpadding="0" cellspacing="0" border="0"><tr>
          <td valign="middle" style="padding-right:8px;">${emailIcon(r.icon, { size: 15, color: "gray" })}</td>
          <td valign="middle" style="font-size:13px;color:${BRAND.textMuted};white-space:nowrap;">${r.label}</td>
        </tr></table>
      </td>
      <td align="right" valign="middle" style="font-size:13px;font-weight:600;color:${BRAND.textDark};padding-left:12px;">${r.value}</td>
    </tr></table>
  </td></tr>`
    )
    .join("");
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" style="background:${BRAND.infoCardBg};border-radius:12px;margin-bottom:22px;">${rowsHtml}</table>`;
}

export function emailNote(html: string, tone: "success" | "danger" | "warning" | "neutral" = "neutral", icon?: IconName): string {
  const palette = {
    success: { bg: BRAND.successBg, color: BRAND.success, iconColor: "success" as IconColor },
    danger: { bg: BRAND.dangerBg, color: BRAND.danger, iconColor: "danger" as IconColor },
    warning: { bg: BRAND.warningBg, color: BRAND.warning, iconColor: "warning" as IconColor },
    neutral: { bg: BRAND.infoCardBg, color: BRAND.textDark, iconColor: "dark" as IconColor },
  }[tone];
  const iconCell = icon
    ? `<td valign="top" style="padding-right:10px;padding-top:1px;">${emailIcon(icon, { size: 15, color: palette.iconColor })}</td>`
    : "";
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" style="background:${palette.bg};border-radius:10px;margin-bottom:20px;"><tr><td style="padding:13px 16px;">
    <table role="presentation" cellpadding="0" cellspacing="0" border="0"><tr>${iconCell}<td style="font-size:13px;color:${palette.color};line-height:1.6;">${html}</td></tr></table>
  </td></tr></table>`;
}

export function emailStatusPill(label: string, tone: "success" | "danger" | "warning" | "neutral" = "neutral"): string {
  const palette = {
    success: { bg: BRAND.successBg, color: BRAND.success },
    danger: { bg: BRAND.dangerBg, color: BRAND.danger },
    warning: { bg: BRAND.warningBg, color: BRAND.warning },
    neutral: { bg: BRAND.infoCardBg, color: BRAND.textDark },
  }[tone];
  return `<table role="presentation" cellpadding="0" cellspacing="0" border="0" style="display:inline-table;"><tr><td style="background:${palette.bg};color:${palette.color};font-size:12px;font-weight:600;padding:5px 14px;border-radius:999px;">${label}</td></tr></table>`;
}

export function emailCodeBox(code: string): string {
  return `<table role="presentation" width="100%" cellpadding="0" cellspacing="0" border="0" style="background:${BRAND.infoCardBg};border:2px dashed ${BRAND.cardBorder};border-radius:12px;margin-bottom:22px;"><tr><td align="center" style="padding:20px;">
    <span style="font-size:34px;font-weight:800;letter-spacing:10px;color:${BRAND.navy};">${code}</span>
  </td></tr></table>`;
}

export function emailButton(label: string, url: string, color: string = BRAND.cta, icon?: IconName): string {
  const iconCell = icon
    ? `<td valign="middle" style="padding-right:8px;">${emailIcon(icon, { size: 15, color: "white" })}</td>`
    : "";
  return `<table role="presentation" cellpadding="0" cellspacing="0" border="0" style="display:inline-table;"><tr><td style="background:${color};border-radius:10px;">
    <a href="${url}" style="display:block;text-decoration:none;padding:13px 32px;">
      <table role="presentation" cellpadding="0" cellspacing="0" border="0"><tr>${iconCell}<td valign="middle" style="color:#ffffff;font-size:14px;font-weight:600;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,Helvetica,Arial,sans-serif;">${label}</td></tr></table>
    </a>
  </td></tr></table>`;
}
