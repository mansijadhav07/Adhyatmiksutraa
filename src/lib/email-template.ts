// Shared visual design system for every transactional email Pratipal sends.
// Build new emails with renderEmailLayout()/emailInfoCard()/emailButton()
// instead of hand-rolled markup, so every email — order, auth, invitation,
// booking — shares one identical shell, logo placement, and color language.
//
// Three hard rules, all learned from real broken test sends:
// 1. No emoji anywhere (subjects or body) — colorful pictographs are a known
//    spam-filter signal and render inconsistently across clients. Icons below
//    are plain monochrome glyphs instead.
// 2. No CSS flexbox/grid. Gmail's mobile apps strip `display:flex` entirely,
//    collapsing rows into unspaced inline text. Every layout here is built
//    with <table>/<td valign="middle"> — the actual cross-client-safe way to
//    align and space content in HTML email.
// 3. No inline <svg>. Gmail (web + mobile) strips inline SVG from the message
//    body outright. Icons are pre-rendered PNGs hosted at
//    pratipal.in/assets/email-icons/<color>/<name>.png (generated by
//    scripts/generate-email-icons.ts) and referenced via plain <img>, same as
//    the logo already is.

