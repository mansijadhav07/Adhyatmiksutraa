export type IconName = keyof typeof ICON_PATHS;

const ICON_COLORS = {
  white: "#ffffff",
  gray: "#6b7280",
  dark: "#1f2430",
  success: "#15803d",
  danger: "#b91c1c",
  warning: "#b45309",
} as const;

export type IconColor = keyof typeof ICON_COLORS;

export const ALL_ICON_NAMES = Object.keys(ICON_PATHS) as IconName[];
export const ALL_ICON_COLORS = Object.keys(ICON_COLORS) as IconColor[];

const EMAIL_ICON_BASE_URL = "https://adhyatmiksutraa.com/assets/email-icons";

/**
 * Raw inline SVG markup for one icon+color combo — used ONLY by
 * scripts/generate-email-icons.ts to rasterize the PNG files served in
 * actual emails. Never reference this directly from a template; use
 * emailIcon() below, which emits an <img> tag instead.
 */
export function buildIconSvgMarkup(name: IconName, color: IconColor, size = 96): string {
  const path = ICON_PATHS[name] || ICON_PATHS.check;
  const hex = ICON_COLORS[color];
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${hex}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${path}</svg>`;
}

export function emailIcon(name: IconName, opts?: { size?: number; color?: IconColor }): string {
  const { size = 20, color = "gray" } = opts || {};
  return `<img src="${EMAIL_ICON_BASE_URL}/${color}/${name}.png" width="${size}" height="${size}" alt="" style="display:block;border:0;outline:none;" />`;
}

// ─── Layout primitives (table-based — no flex/grid) ────────────────────────

export interface EmailLayoutOptions {
  /** Hidden preview text shown next to the subject in inbox lists. */
  preheader?: string;
  badgeIcon: IconName;
  heading: string;
  subheading?: string;
  bodyHtml: string;
  cta?: { label: string; url: string; icon?: IconName };
  /** Overrides the default "Questions? Reach us at..." footer line. */
  footerNote?: string;
}

